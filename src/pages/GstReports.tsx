// Convert sheet data into exportMultiSheet format
function toExportSheet(name: string, headers: string[], rows: (string | number)[][], widths?: number[]) {
  const data = rows.map((row) => {
    const obj: Record<string, any> = {};
    headers.forEach((h, i) => { obj[h] = row[i] ?? ""; });
    return obj;
  });
  const columns = headers.map((h, i) => ({
    key: h,
    label: h,
    width: widths?.[i] ?? Math.max(h.length + 2, 14),
  }));
  return { sheetName: name, columns, data };
}

// ── Preview Dialog ────────────────────────────────────────────────────────────

function PreviewDialog({
  open,
  onClose,
  title,
  sheets,
}: {
  open: boolean;
  onClose: () => void;
  title: string;
  sheets: PreviewSheet[];
}) {
  const [activeTab, setActiveTab] = useState(0);
  const sheet = sheets[activeTab];
  const PREVIEW_LIMIT = 50;

  return (
    <Dialog open={open} onOpenChange={(o) => !o && onClose()}>
      <DialogContent className="max-w-6xl w-full max-h-[85vh] flex flex-col p-0">
        <DialogHeader className="px-6 pt-5 pb-3 border-b border-border shrink-0">
          <div className="flex items-center justify-between">
            <DialogTitle className="text-base font-semibold">{title} — Preview</DialogTitle>
            <button onClick={onClose} className="text-muted-foreground hover:text-foreground">
              <X className="h-4 w-4" />
            </button>
          </div>
          {sheets.length > 1 && (
            <div className="flex gap-1 mt-2 flex-wrap">
              {sheets.map((s, i) => (
                <button
                  key={s.name}
                  onClick={() => setActiveTab(i)}
                  className={`text-xs px-3 py-1 rounded-full border transition-colors ${
                    i === activeTab
                      ? "bg-blue-600 text-white border-blue-600"
                      : "border-border text-muted-foreground hover:text-foreground"
                  }`}
                >
                  {s.name}
                </button>
              ))}
            </div>
          )}
        </DialogHeader>

        {sheet && (
          <div className="flex-1 overflow-auto p-4">
            <div className="flex items-center justify-between mb-2">
              <p className="text-xs text-muted-foreground">
                Showing first {Math.min(sheet.rows.length, PREVIEW_LIMIT)} of {sheet.totalRows} rows
              </p>
              <p className="text-xs text-amber-600 font-medium">{DISCLAIMER}</p>
            </div>
            <div className="overflow-x-auto rounded-lg border border-border">
              <table className="w-full text-xs">
                <thead className="bg-slate-800 text-white">
                  <tr>
                    {sheet.headers.map((h) => (
                      <th key={h} className="px-2 py-1.5 text-left whitespace-nowrap font-medium">
                        {h}
                      </th>
                    ))}
                  </tr>
                </thead>
                <tbody>
                  {sheet.rows.slice(0, PREVIEW_LIMIT).map((row, i) => (
                    <tr key={i} className={i % 2 === 0 ? "bg-white" : "bg-slate-50"}>
                      {row.map((cell, j) => (
                        <td key={j} className="px-2 py-1 border-b border-border/50 whitespace-nowrap">
                          {cell ?? ""}
                        </td>
                      ))}
                    </tr>
                  ))}
                  {sheet.rows.length === 0 && (
                    <tr>
                      <td colSpan={sheet.headers.length} className="text-center py-8 text-muted-foreground">
                        No data for this period
                      </td>
                    </tr>
                  )}
                </tbody>
              </table>
            </div>
          </div>
        )}
      </DialogContent>
    </Dialog>
  );
}


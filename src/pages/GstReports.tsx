function round2(n: number): number {
  return Math.round((n ?? 0) * 100) / 100;
}

function extractStateCode(pos: string | null | undefined): string {
  if (!pos) return "";
  const m = pos.match(/^(\d{2})/);
  return m ? m[1] : pos.slice(0, 2);
}

function getDateBounds(
  range: DateRange,
  fyYear: string | null
): { start: string; end: string; label: string } {
  const now = new Date();
  if (range === "this_month") {
    return {
      start: format(startOfMonth(now), "yyyy-MM-dd"),
      end: format(now, "yyyy-MM-dd"),
      label: format(now, "MMMM yyyy"),
    };
  }
  if (range === "last_month") {
    const prev = subMonths(now, 1);
    return {
      start: format(startOfMonth(prev), "yyyy-MM-dd"),
      end: format(endOfMonth(prev), "yyyy-MM-dd"),
      label: format(prev, "MMMM yyyy"),
    };
  }
  if (range === "this_quarter") {
    return {
      start: format(startOfQuarter(now), "yyyy-MM-dd"),
      end: format(now, "yyyy-MM-dd"),
      label: `Q${Math.ceil((now.getMonth() + 1) / 3)} ${now.getFullYear()}`,
    };
  }
  // this_fy — use company FY if available
  if (fyYear && /^\d{2}-\d{2}$/.test(fyYear)) {
    const startYear = 2000 + parseInt(fyYear.slice(0, 2));
    const endYear = startYear + 1;
    return {
      start: `${startYear}-04-01`,
      end: `${endYear}-03-31`,
      label: `FY ${fyYear}`,
    };
  }
  const fy = now.getMonth() >= 3 ? now.getFullYear() : now.getFullYear() - 1;
  return {
    start: `${fy}-04-01`,
    end: format(now, "yyyy-MM-dd"),
    label: `FY ${String(fy).slice(2)}-${String(fy + 1).slice(2)}`,
  };
}

function slugLabel(label: string): string {
  return label.replace(/\s+/g, "").replace(/[^A-Za-z0-9]/g, "");
}


async function buildItcData(start: string, end: string) {
  const { data: grns } = await (supabase as any)
    .from("grns")
    .select("id, grn_number, grn_date, vendor_name, vendor_invoice_number, vendor_invoice_date, po_id")
    .gte("grn_date", start)
    .lte("grn_date", end)
    .not("status", "in", "(cancelled,deleted,draft)");

  const grnData = (grns ?? []) as any[];
  const poIds = [...new Set(grnData.map((g) => g.po_id).filter(Boolean))];

  const poMap = new Map<string, any>();
  if (poIds.length > 0) {
    const { data: pos } = await supabase
      .from("purchase_orders")
      .select("id, vendor_name, vendor_gstin, grand_total, igst_amount, cgst_amount, sgst_amount")
      .in("id", poIds as string[]);
    for (const po of (pos ?? []) as any[]) poMap.set(po.id, po);
  }

  return grnData.map((grn) => {
    const po = poMap.get(grn.po_id) ?? {};
    return {
      grn_number: grn.grn_number ?? "",
      grn_date: formatDateGST(grn.grn_date),
      vendor_name: po.vendor_name || grn.vendor_name || "",
      vendor_gstin: po.vendor_gstin ?? "",
      vendor_invoice: grn.vendor_invoice_number ?? "",
      invoice_date: formatDateGST(grn.vendor_invoice_date),
      invoice_value: round2(po.grand_total ?? 0),
      igst: round2(po.igst_amount ?? 0),
      cgst: round2(po.cgst_amount ?? 0),
      sgst: round2(po.sgst_amount ?? 0),
      total_itc: round2((po.igst_amount ?? 0) + (po.cgst_amount ?? 0) + (po.sgst_amount ?? 0)),
    };
  });
}


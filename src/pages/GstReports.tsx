interface ReportCardProps {
  icon: React.ReactNode;
  title: string;
  description: string;
  onDownload: (range: DateRange) => Promise<void>;
  onPreview: (range: DateRange) => Promise<PreviewSheet[]>;
}

function ReportCard({ icon, title, description, onDownload, onPreview }: ReportCardProps) {
  const [range, setRange] = useState<DateRange>("this_month");
  const [loading, setLoading] = useState(false);
  const [previewing, setPreviewing] = useState(false);
  const [previewSheets, setPreviewSheets] = useState<PreviewSheet[]>([]);
  const [previewOpen, setPreviewOpen] = useState(false);

  const handleDownload = async () => {
    setLoading(true);
    try { await onDownload(range); } finally { setLoading(false); }
  };

  const handlePreview = async () => {
    setPreviewing(true);
    try {
      const sheets = await onPreview(range);
      setPreviewSheets(sheets);
      setPreviewOpen(true);
    } finally {
      setPreviewing(false);
    }
  };

  return (
    <>
      <div className="paper-card flex flex-col gap-4">
        <div className="flex items-start gap-3">
          <div className="rounded-lg bg-blue-50 p-2 shrink-0">{icon}</div>
          <div>
            <h3 className="font-semibold text-slate-900 text-sm">{title}</h3>
            <p className="text-xs text-muted-foreground mt-0.5 leading-relaxed">{description}</p>
          </div>
        </div>
        <div className="flex items-center gap-2 mt-auto flex-wrap">
          <Select value={range} onValueChange={(v) => setRange(v as DateRange)}>
            <SelectTrigger className="h-8 text-xs flex-1 min-w-[110px]">
              <SelectValue />
            </SelectTrigger>
            <SelectContent>
              <SelectItem value="this_month">This Month</SelectItem>
              <SelectItem value="last_month">Last Month</SelectItem>
              <SelectItem value="this_quarter">This Quarter</SelectItem>
              <SelectItem value="this_fy">This Financial Year</SelectItem>
            </SelectContent>
          </Select>
          <Button
            size="sm"
            variant="outline"
            className="h-8 gap-1.5 shrink-0"
            onClick={handlePreview}
            disabled={previewing || loading}
          >
            <Eye className="h-3.5 w-3.5" />
            {previewing ? "Loading…" : "Preview"}
          </Button>
          <Button
            size="sm"
            className="h-8 gap-1.5 bg-blue-600 hover:bg-blue-700 text-white shrink-0"
            onClick={handleDownload}
            disabled={loading || previewing}
          >
            <Download className="h-3.5 w-3.5" />
            {loading ? "Generating…" : "Download"}
          </Button>
        </div>
      </div>

      <PreviewDialog
        open={previewOpen}
        onClose={() => setPreviewOpen(false)}
        title={title}
        sheets={previewSheets}
      />
    </>
  );
}

// ── Main Page ─────────────────────────────────────────────────────────────────

export default function GstReports() {
  const { toast } = useToast();

  const { data: companySettings } = useQuery({
    queryKey: ["company-settings"],
    queryFn: fetchCompanySettings,
  });

  const fyYear = companySettings?.fy_year ?? null;
  const companyStateCode = companySettings?.state_code ?? "";

  const handleError = (title: string) => {
    toast({ title, description: "Check console for details.", variant: "destructive" });
  };

  // ── GSTR-1 ────────────────────────────────────────────────────────────────

  const previewGstr1 = async (range: DateRange): Promise<PreviewSheet[]> => {
    const { start, end } = getDateBounds(range, fyYear);
    const { b2b, b2cLarge, hsn, nil } = await buildGstr1Data(start, end, companyStateCode);
    return [
      { name: "B2B Invoices", headers: GSTR1_B2B_HEADERS, rows: b2b.map(b2bToRow), totalRows: b2b.length },
      { name: "B2C Large", headers: GSTR1_B2CLARGE_HEADERS, rows: b2cLarge.map(b2cToRow), totalRows: b2cLarge.length },
      { name: "HSN Summary", headers: HSN_HEADERS, rows: hsn.map(hsnToRow), totalRows: hsn.length },
      { name: "Nil/Exempt", headers: NIL_HEADERS, rows: nil.map(nilToRow), totalRows: nil.length },
    ];
  };

  const downloadGstr1 = async (range: DateRange) => {
    try {
      const { start, end, label } = getDateBounds(range, fyYear);
      const { b2b, b2cLarge, hsn, nil } = await buildGstr1Data(start, end, companyStateCode);
      exportMultiSheet(
        [
          toExportSheet("B2B Invoices", GSTR1_B2B_HEADERS, b2b.map(b2bToRow)),
          toExportSheet("B2C Large", GSTR1_B2CLARGE_HEADERS, b2cLarge.map(b2cToRow)),
          toExportSheet("HSN Summary", HSN_HEADERS, hsn.map(hsnToRow)),
          toExportSheet("Nil-Exempt-NonGST", NIL_HEADERS, nil.map(nilToRow)),
        ],
        `BizDocs_GSTR1_${slugLabel(label)}.xlsx`,
        DISCLAIMER
      );
    } catch { handleError("Failed to generate GSTR-1"); }
  };

  // ── GSTR-2 ────────────────────────────────────────────────────────────────

  const previewGstr2 = async (range: DateRange): Promise<PreviewSheet[]> => {
    const { start, end } = getDateBounds(range, fyYear);
    const rows = await buildGstr2Data(start, end, companyStateCode);
    return [{ name: "B2B Purchases", headers: GSTR2_B2B_HEADERS, rows: rows.map(gstr2ToRow), totalRows: rows.length }];
  };

  const downloadGstr2 = async (range: DateRange) => {
    try {
      const { start, end, label } = getDateBounds(range, fyYear);
      const rows = await buildGstr2Data(start, end, companyStateCode);
      exportMultiSheet(
        [toExportSheet("B2B Purchases", GSTR2_B2B_HEADERS, rows.map(gstr2ToRow))],
        `BizDocs_GSTR2_${slugLabel(label)}.xlsx`,
        DISCLAIMER
      );
    } catch { handleError("Failed to generate GSTR-2"); }
  };

  // ── HSN Summary ───────────────────────────────────────────────────────────

  const previewHsn = async (range: DateRange): Promise<PreviewSheet[]> => {
    const { start, end } = getDateBounds(range, fyYear);
    const rows = await buildHsnData(start, end);
    return [{ name: "HSN Summary", headers: HSN_HEADERS, rows: rows.map(hsnToRow), totalRows: rows.length }];
  };

  const downloadHsn = async (range: DateRange) => {
    try {
      const { start, end, label } = getDateBounds(range, fyYear);
      const rows = await buildHsnData(start, end);
      if (rows.length === 0) { toast({ title: "No invoices in this period" }); return; }
      exportMultiSheet(
        [toExportSheet("HSN Summary", HSN_HEADERS, rows.map(hsnToRow))],
        `BizDocs_HSN_${slugLabel(label)}.xlsx`,
        DISCLAIMER
      );
    } catch { handleError("Failed to generate HSN Summary"); }
  };

  // ── ITC Register ──────────────────────────────────────────────────────────

  const ITC_HEADERS = [
    "GRN Number", "GRN Date", "Vendor Name", "Vendor GSTIN",
    "Vendor Invoice", "Invoice Date", "Invoice Value",
    "IGST", "CGST", "SGST", "Total ITC",
  ];
  const itcToRow = (r: any): (string | number)[] => [
    r.grn_number, r.grn_date, r.vendor_name, r.vendor_gstin,
    r.vendor_invoice, r.invoice_date, r.invoice_value,
    r.igst, r.cgst, r.sgst, r.total_itc,
  ];

  const previewItc = async (range: DateRange): Promise<PreviewSheet[]> => {
    const { start, end } = getDateBounds(range, fyYear);
    const rows = await buildItcData(start, end);
    return [{ name: "ITC Register", headers: ITC_HEADERS, rows: rows.map(itcToRow), totalRows: rows.length }];
  };

  const downloadItc = async (range: DateRange) => {
    try {
      const { start, end, label } = getDateBounds(range, fyYear);
      const rows = await buildItcData(start, end);
      exportMultiSheet(
        [toExportSheet("ITC Register", ITC_HEADERS, rows.map(itcToRow))],
        `BizDocs_ITC_${slugLabel(label)}.xlsx`,
        DISCLAIMER
      );
    } catch { handleError("Failed to generate ITC Register"); }
  };

  // ── E-way Bill ────────────────────────────────────────────────────────────

  const EWAY_HEADERS = [
    "DC No", "Date", "Party Name", "GSTIN",
    "From State", "To State", "Description", "HSN", "Qty", "Value", "Vehicle No",
  ];

  const previewEway = async (range: DateRange): Promise<PreviewSheet[]> => {
    const rows = await fetchEwayRows(range);
    return [{ name: "E-way Bill Data", headers: EWAY_HEADERS, rows, totalRows: rows.length }];
  };

  const downloadEway = async (range: DateRange) => {
    try {
      const { label } = getDateBounds(range, fyYear);
      const rows = await fetchEwayRows(range);
      exportMultiSheet(
        [toExportSheet("E-way Bill Data", EWAY_HEADERS, rows)],
        `BizDocs_EwayBill_${slugLabel(label)}.xlsx`,
        DISCLAIMER
      );
    } catch { handleError("Failed to generate E-way Bill data"); }
  };

  const fetchEwayRows = async (range: DateRange) => {
    const { start, end } = getDateBounds(range, fyYear);
    const { data: dcs } = await supabase
      .from("delivery_challans")
      .select("id, dc_number, dc_date, party_name, party_gstin, vehicle_number")
      .gte("dc_date", start)
      .lte("dc_date", end)
      .neq("status", "cancelled")
      .order("dc_date", { ascending: true });

    const dcData = (dcs ?? []) as any[];
    const dcIds = dcData.map((d) => d.id);
    let lineItems: any[] = [];
    if (dcIds.length > 0) {
      const { data: li } = await (supabase as any)
        .from("dc_line_items")
        .select("dc_id, description, hsn_sac_code, qty_nos, approximate_value")
        .in("dc_id", dcIds);
      lineItems = li ?? [];
    }

    const dcMap = new Map(dcData.map((d) => [d.id, d]));
    return lineItems.map((li): (string | number)[] => {
      const dc = dcMap.get(li.dc_id) as any;
      return [
        dc?.dc_number ?? "", formatDateGST(dc?.dc_date),
        dc?.party_name ?? "", dc?.party_gstin ?? "",
        "", "",  // from_state, to_state — not in table
        li.description ?? "", li.hsn_sac_code ?? "",
        li.qty_nos ?? 0, li.approximate_value ?? 0,
        dc?.vehicle_number ?? "",
      ];
    });
  };

  // ── Job Work ──────────────────────────────────────────────────────────────

  const JW_HEADERS = [
    "DC No", "Date", "Vendor Name", "Vendor GSTIN",
    "Component", "Qty Sent", "Qty Returned", "Pending Qty", "Days Out",
  ];

  const previewJobWork = async (range: DateRange): Promise<PreviewSheet[]> => {
    const rows = await fetchJobWorkRows(range);
    return [{ name: "Job Work Register", headers: JW_HEADERS, rows, totalRows: rows.length }];
  };

  const downloadJobWork = async (range: DateRange) => {
    try {
      const { label } = getDateBounds(range, fyYear);
      const rows = await fetchJobWorkRows(range);
      exportMultiSheet(
        [toExportSheet("Job Work Register", JW_HEADERS, rows)],
        `BizDocs_JobWork_${slugLabel(label)}.xlsx`,
        DISCLAIMER
      );
    } catch { handleError("Failed to generate Job Work Register"); }
  };

  const fetchJobWorkRows = async (range: DateRange) => {
    const { start, end } = getDateBounds(range, fyYear);
    const { data: dcs } = await supabase
      .from("delivery_challans")
      .select("id, dc_number, dc_date, party_name, party_gstin, return_due_date, status")
      .eq("dc_type" as any, "returnable")
      .gte("dc_date", start)
      .lte("dc_date", end)
      .order("dc_date", { ascending: true });

    const dcData = (dcs ?? []) as any[];
    const dcIds = dcData.map((d) => d.id);
    let lineItems: any[] = [];
    if (dcIds.length > 0) {
      const { data: li } = await (supabase as any)
        .from("dc_line_items")
        .select("dc_id, description, qty_nos, returned_qty_nos")
        .in("dc_id", dcIds);
      lineItems = li ?? [];
    }

    const dcMap = new Map(dcData.map((d) => [d.id, d]));
    return lineItems.map((li): (string | number)[] => {
      const dc = dcMap.get(li.dc_id) as any;
      const sent = li.qty_nos ?? 0;
      const returned = li.returned_qty_nos ?? 0;
      const daysOut = dc?.dc_date
        ? Math.max(0, Math.floor((Date.now() - new Date(dc.dc_date).getTime()) / 86400000))
        : 0;
      return [
        dc?.dc_number ?? "", formatDateGST(dc?.dc_date),
        dc?.party_name ?? "", dc?.party_gstin ?? "",
        li.description ?? "", sent, returned, Math.max(0, sent - returned), daysOut,
      ];
    });
  };

  // ── Render ────────────────────────────────────────────────────────────────

  const cardIcon = <FileSpreadsheet className="h-4 w-4 text-blue-600" />;

  return (
    <div className="p-4 md:p-6 space-y-6">
      <div>
        <h1 className="text-2xl font-bold text-slate-900 flex items-center gap-2">
          <FileSpreadsheet className="h-5 w-5 text-blue-600" />
          GST Reports
        </h1>
        <p className="text-sm text-slate-500 mt-1">
          GST portal-compliant Excel files · FY {fyYear ?? "—"} · State: {companyStateCode || "—"}
        </p>
      </div>

      <div className="flex items-start gap-3 bg-blue-50 border border-blue-200 rounded-lg p-4 text-blue-800 text-sm">
        <Info className="h-4 w-4 shrink-0 mt-0.5 text-blue-600" />
        <p>
          Reports match GST portal column formats. Use <strong>Preview</strong> to verify before downloading.
          Always reconcile with your CA before filing.
        </p>
      </div>

      <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
        <ReportCard
          icon={cardIcon}
          title="GSTR-1 (Outward Supplies)"
          description="4-sheet portal-format file: B2B invoices, B2C Large, HSN Summary, Nil/Exempt. One row per invoice per rate."
          onDownload={downloadGstr1}
          onPreview={previewGstr1}
        />
        <ReportCard
          icon={cardIcon}
          title="GSTR-2 / ITC (Inward Supplies)"
          description="B2B purchase register with all 19 portal columns: supplier GSTIN, invoice details, rate-wise ITC breakup."
          onDownload={downloadGstr2}
          onPreview={previewGstr2}
        />
        <ReportCard
          icon={cardIcon}
          title="HSN Summary"
          description="Standalone HSN summary with quantity, total value, taxable value, IGST, CGST, SGST and Cess."
          onDownload={downloadHsn}
          onPreview={previewHsn}
        />
        <ReportCard
          icon={cardIcon}
          title="ITC Register (GRN-based)"
          description="Input tax credit register built from received GRNs. Shows vendor invoice details and ITC amounts per receipt."
          onDownload={downloadItc}
          onPreview={previewItc}
        />
        <ReportCard
          icon={cardIcon}
          title="E-way Bill Data"
          description="DC data formatted for e-way bill reference. Includes HSN, vehicle number, party GSTIN and values."
          onDownload={downloadEway}
          onPreview={previewEway}
        />
        <ReportCard
          icon={cardIcon}
          title="Job Work Register (Section 143)"
          description="All returnable DC components sent for job work — quantity sent, returned, pending, and days outstanding."
          onDownload={downloadJobWork}
          onPreview={previewJobWork}
        />
      </div>
    </div>
  );
}

async function buildGstr1Data(start: string, end: string, companyStateCode: string) {
  const { data: invoices } = await supabase
    .from("invoices")
    .select("id, invoice_number, invoice_date, customer_name, customer_gstin, place_of_supply, grand_total")
    .gte("invoice_date", start)
    .lte("invoice_date", end)
    .not("status", "in", "(cancelled,deleted)")
    .order("invoice_date", { ascending: true });

  const invData = (invoices ?? []) as any[];
  const invIds = invData.map((i) => i.id);
  let lineItems: any[] = [];
  if (invIds.length > 0) {
    const { data: li } = await (supabase as any)
      .from("invoice_line_items")
      .select("invoice_id, hsn_sac_code, description, unit, quantity, gst_rate, taxable_amount, cgst_amount, sgst_amount, igst_amount, line_total")
      .in("invoice_id", invIds);
    lineItems = li ?? [];
  }

  const invMap = new Map<string, any>(invData.map((i) => [i.id, i]));

  const b2bMap = new Map<string, any>();
  const b2cLargeMap = new Map<string, any>();
  const hsnMap = new Map<string, any>();
  const nilAggMap = new Map<string, { type: string; nil_rated: number; exempted: number; non_gst: number }>();

  for (const li of lineItems) {
    const inv = invMap.get(li.invoice_id);
    if (!inv) continue;

    const posCode = extractStateCode(inv.place_of_supply);
    const isB2B = !!(inv.customer_gstin?.trim());
    const isInterState = posCode !== companyStateCode;
    const rate = li.gst_rate ?? 0;
    const taxable = round2(li.taxable_amount ?? 0);

    // B2B
    if (isB2B) {
      const key = `${li.invoice_id}|${rate}`;
      if (!b2bMap.has(key)) {
        b2bMap.set(key, {
          gstin: inv.customer_gstin ?? "",
          name: inv.customer_name ?? "",
          invoice_number: inv.invoice_number ?? "",
          invoice_date: formatDateGST(inv.invoice_date),
          invoice_value: round2(inv.grand_total ?? 0),
          place_of_supply: posCode || inv.place_of_supply || "",
          reverse_charge: "N",
          invoice_type: "Regular",
          ecomm_gstin: "",
          rate,
          taxable_value: 0,
          cess_amount: 0,
        });
      }
      b2bMap.get(key).taxable_value += taxable;
    }

    // B2C Large (inter-state B2C with invoice value > 2.5 lakh)
    if (!isB2B && isInterState && (inv.grand_total ?? 0) > 250000) {
      const key = `${posCode}|${rate}`;
      if (!b2cLargeMap.has(key)) {
        b2cLargeMap.set(key, {
          type: "OE",
          place_of_supply: posCode || inv.place_of_supply || "",
          applicable_pct: "",
          rate,
          taxable_value: 0,
          cess_amount: 0,
          ecomm_gstin: "",
        });
      }
      b2cLargeMap.get(key).taxable_value += taxable;
    }

    // HSN Summary
    const hsnKey = `${li.hsn_sac_code ?? ""}|${rate}`;
    if (!hsnMap.has(hsnKey)) {
      hsnMap.set(hsnKey, {
        hsn: li.hsn_sac_code ?? "",
        description: li.description ?? "",
        uqc: li.unit ?? "NOS",
        total_quantity: 0,
        total_value: 0,
        taxable_value: 0,
        integrated_tax: 0,
        central_tax: 0,
        state_ut_tax: 0,
        cess: 0,
      });
    }
    const hsnRow = hsnMap.get(hsnKey);
    hsnRow.total_quantity += li.quantity ?? 0;
    hsnRow.total_value += round2(li.line_total ?? 0);
    hsnRow.taxable_value += taxable;
    hsnRow.integrated_tax += round2(li.igst_amount ?? 0);
    hsnRow.central_tax += round2(li.cgst_amount ?? 0);
    hsnRow.state_ut_tax += round2(li.sgst_amount ?? 0);

    // Nil / Zero-rated
    if (rate === 0 && taxable > 0) {
      const nilKey = isB2B ? "B2B" : "B2C";
      if (!nilAggMap.has(nilKey)) {
        nilAggMap.set(nilKey, { type: nilKey, nil_rated: 0, exempted: 0, non_gst: 0 });
      }
      nilAggMap.get(nilKey)!.nil_rated += taxable;
    }
  }

  const b2b = [...b2bMap.values()].map((r) => ({ ...r, taxable_value: round2(r.taxable_value) }));
  const b2cLarge = [...b2cLargeMap.values()].map((r) => ({ ...r, taxable_value: round2(r.taxable_value) }));
  const hsn = [...hsnMap.values()].map((r) => ({
    ...r,
    total_value: round2(r.total_value),
    taxable_value: round2(r.taxable_value),
    integrated_tax: round2(r.integrated_tax),
    central_tax: round2(r.central_tax),
    state_ut_tax: round2(r.state_ut_tax),
  }));
  const nil = [...nilAggMap.values()].map((r) => ({
    ...r,
    nil_rated: round2(r.nil_rated),
  }));

  return { b2b, b2cLarge, hsn, nil };
}

async function buildGstr2Data(start: string, end: string, companyStateCode: string) {
  // Use purchase orders as the base source (they have the complete GST data)
  const { data: pos } = await supabase
    .from("purchase_orders")
    .select("id, po_number, po_date, vendor_name, vendor_gstin, vendor_state_code, grand_total")
    .gte("po_date", start)
    .lte("po_date", end)
    .not("status", "in", "(cancelled,deleted)")
    .order("po_date", { ascending: true });

  const poData = (pos ?? []) as any[];
  const poIds = poData.map((p) => p.id);

  // Fetch the vendor_invoice_number from linked GRNs (best effort)
  const grnInvMap = new Map<string, { invoice_number: string; invoice_date: string }>();
  if (poIds.length > 0) {
    const { data: grns } = await (supabase as any)
      .from("grns")
      .select("po_id, vendor_invoice_number, vendor_invoice_date")
      .in("po_id", poIds)
      .not("status", "in", "(cancelled,deleted)");
    for (const g of (grns ?? []) as any[]) {
      if (g.po_id && g.vendor_invoice_number) {
        grnInvMap.set(g.po_id, {
          invoice_number: g.vendor_invoice_number,
          invoice_date: g.vendor_invoice_date || "",
        });
      }
    }
  }

  let lineItems: any[] = [];
  if (poIds.length > 0) {
    const { data: li } = await (supabase as any)
      .from("po_line_items")
      .select("po_id, description, quantity, unit_price, gst_rate, line_total")
      .in("po_id", poIds);
    lineItems = li ?? [];
  }

  const poMap = new Map<string, any>(poData.map((p) => [p.id, p]));

  // Group by PO + rate
  const rowMap = new Map<string, any>();
  for (const li of lineItems) {
    const po = poMap.get(li.po_id);
    if (!po) continue;

    const rate = li.gst_rate ?? 0;
    const key = `${li.po_id}|${rate}`;
    const taxable = round2(li.line_total ?? (li.quantity ?? 0) * (li.unit_price ?? 0));
    const vendorStateCode = extractStateCode(po.vendor_gstin) || po.vendor_state_code || "";
    const isInterState = vendorStateCode !== companyStateCode;
    const gstAmt = round2(taxable * (rate / 100));
    const igst = isInterState ? gstAmt : 0;
    const cgst = isInterState ? 0 : round2(gstAmt / 2);
    const sgst = isInterState ? 0 : round2(gstAmt / 2);
    const grnInfo = grnInvMap.get(li.po_id);

    if (!rowMap.has(key)) {
      rowMap.set(key, {
        gstin_supplier: po.vendor_gstin ?? "",
        supplier_name: po.vendor_name ?? "",
        invoice_number: grnInfo?.invoice_number || po.po_number || "",
        invoice_date: formatDateGST(grnInfo?.invoice_date || po.po_date),
        invoice_value: round2(po.grand_total ?? 0),
        place_of_supply: companyStateCode,
        reverse_charge: "N",
        invoice_type: "Regular",
        rate,
        taxable_value: 0,
        integrated_tax: 0,
        central_tax: 0,
        state_ut_tax: 0,
        cess: 0,
        eligibility: "Input",
        availed_igst: 0,
        availed_cgst: 0,
        availed_sgst: 0,
        availed_cess: 0,
      });
    }
    const row = rowMap.get(key);
    row.taxable_value += taxable;
    row.integrated_tax += igst;
    row.central_tax += cgst;
    row.state_ut_tax += sgst;
    row.availed_igst += igst;
    row.availed_cgst += cgst;
    row.availed_sgst += sgst;
  }

  return [...rowMap.values()].map((r) => ({
    ...r,
    taxable_value: round2(r.taxable_value),
    integrated_tax: round2(r.integrated_tax),
    central_tax: round2(r.central_tax),
    state_ut_tax: round2(r.state_ut_tax),
    availed_igst: round2(r.availed_igst),
    availed_cgst: round2(r.availed_cgst),
    availed_sgst: round2(r.availed_sgst),
  }));
}

